public class Book {
    private String nameBook;

    private int age;

    public Book(String nameBook, int age) {
        this.nameBook = nameBook;
        this.age = age;
    }

    public String getNameBook() {
        return this.nameBook;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }



}
